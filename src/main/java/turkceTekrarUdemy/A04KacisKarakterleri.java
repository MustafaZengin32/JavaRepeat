package turkceTekrarUdemy;

public class A04KacisKarakterleri {
    public static void main(String[] args) {

        System.out.println("Merhaba\nDünya"); // Yeni satıra geçer
        System.out.println("Java\tProgramlama"); // Sekme boşluğu bırakır
        System.out.println("Benim adım \"ChatGPT\"."); // Çift tırnak içinde çift tırnak kullanımı
        System.out.println("Yolu: C:\\users\\kullanici"); // Gerçek bir ters bölü işareti kullanımı
        System.out.println("Dosya adı: file.txt\b\b\b"); // Bir karakteri geri alır
        System.out.println("Java\fGeliştirme"); // Form besleme kullanımı
        System.out.println("av\smevsimi");//bir space
        System.out.println("Kap\rGel");//oncekileri sil
        //System.out.println("al\dgel");

    }
}
