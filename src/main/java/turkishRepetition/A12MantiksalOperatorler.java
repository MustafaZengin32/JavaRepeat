package turkishRepetition;

public class A12MantiksalOperatorler {

    //&& and
    //|| or
    //! not
    public static void main(String[] args) {

        System.out.println(3==3 && 2<3);//true

        System.out.println(3==3 && 2>3);//false

        System.out.println(3==3 && 2<3 && "Murat"=="Murat");//true

        System.out.println(3==3 || 2>3 ||"Mali"=="Mali");//true

        System.out.println(!(3>2));//false








    }

}
